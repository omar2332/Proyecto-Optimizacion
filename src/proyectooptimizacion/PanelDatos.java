    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        // TODO add your handling code here:
        
        
        
        
        fo = jComboBox1.getSelectedIndex() + 1;

        
        for(int i = 0; i<n;i++){
            if(fi[i].getText().isEmpty()){
                c[i] = 0.0;
            }else{
                c[i] = Double.parseDouble(fi[i].getText());
            }
        }
        
        for(int i = 0;i<m;i++){
            r[i] = rest[i].getSelectedIndex() + 1;
            for(int j = 0; j<n+1;j++){
                
                if(j ==  n){
                    a[i][j] = Double.parseDouble(bi[i].getText());
                }
                
                if(j != n){
                    if(entrada[i][j].getText().isEmpty()){
                        a[i][j] = 0.0;
                    }else{
                        a[i][j] = Double.parseDouble(entrada[i][j].getText());
                    }
                }
                
            }
        }
        FO = new funcionObjetivo();
        FramePrincipal.PanelPrincipal.add(FO);
        FO.setBounds(0, 0, 1000, 720);
        FramePrincipal.PA.setVisible(false);
        FramePrincipal.PD.setVisible(false);
        FO.setVisible(true);
        
        
    }//GEN-LAST:event_btnIniciarActionPerformed

    
