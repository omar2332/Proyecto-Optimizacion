    public PanelDatos() {
        initComponents();
        boolean regreso = true;
        
        if(c == null && a == null && r == null ){
            c = new double[n];
            a = new double[m][n+1];
            r = new int [m];
            regreso = false;
            jComboBox1.setSelectedIndex(0);
            
            
        }
        
        if(atras){
            c = new double[n];
            a = new double[m][n+1];
            r = new int [m];
            regreso = false;
            jComboBox1.setSelectedIndex(fo-1);
            
            atras =false;
        }
            
        
        
        
        
        
        entrada =  new javax.swing.JTextField[m][n];
        xs = new javax.swing.JLabel[n];
        fxs = new javax.swing.JLabel[n];
        bi = new javax.swing.JTextField[m];
        fi = new javax.swing.JTextField[n];
        rest = new javax.swing.JComboBox[m];
        panelDatos.setLayout(new GridLayout(m,n,5,5));
        panelFuncion.setLayout(new GridLayout(1,n,10,10));
        
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                xs[j] = new javax.swing.JLabel();
                entrada[i][j] = new javax.swing.JTextField();
                if(regreso){
                    entrada[i][j].setText(String.valueOf(a[i][j]));
                }
                if(j==0){
                    xs[j].setText("X" + (j+1) + "+");
                }else{
                    if(j != (n-1)){
                        xs[j].setText("X" + (j+1) + "+");
                    }else{
                        xs[j].setText("X" + (j+1));
                    }
                    
                }
                
                entrada[i][j].setPreferredSize(new Dimension(20, 20));
                
                panelDatos.add(entrada[i][j]);
                panelDatos.add(xs[j]); //
                
                if(i == 0){
                    fxs[j] = new javax.swing.JLabel();
                    fi[j] = new javax.swing.JTextField();
                    fi[j].setPreferredSize(new Dimension(80, 30));
                    if(regreso){
                    fi[j].setText(String.valueOf(c[i]));
                    }
                    if(j==0){
                        fxs[j].setText("X" + (j+1) + "+");
                    }else{
                        if(j == (n-1)){
                            fxs[j].setText("X" + (j+1));
                        }else{
                            fxs[j].setText("X" + (j+1) + "+");
                        }
                        
                    }
                    panelFuncion.add(fi[j]);
                    panelFuncion.add(fxs[j]);
                }
                
            }
            rest[i] = new javax.swing.JComboBox<>();
            
            
            bi[i] = new javax.swing.JTextField();
            if(regreso){
                bi[i].setText(String.valueOf(a[i][n]));
            }
            
            
            bi[i].setPreferredSize(new Dimension(30, 30));
            
            rest[i].setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { "<=","=",">=" }));
            if(regreso){
                rest[i].setSelectedIndex(r[i] - 1);
            }
            panelDatos.add(rest[i]);
            panelDatos.add(bi[i]);
            
        }
        
       
        
    }

